    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       String currentPass = new String(jPasswordField1.getPassword()).trim();
        String newPass = new String(jPasswordField2.getPassword()).trim();
        String confirmPass = new String(jPasswordField3.getPassword()).trim();
        
        if (currentPass.isEmpty() || newPass.isEmpty() || confirmPass.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all fields", "Warning", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (!newPass.equals(confirmPass)) {
            javax.swing.JOptionPane.showMessageDialog(this, "New passwords do not match", "Warning", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            java.sql.Connection conn = DatabaseConnection.getConnection();
            java.sql.PreparedStatement ps = conn.prepareStatement("SELECT * FROM USERS WHERE PASSWORD=?");
            ps.setString(1, currentPass);
            java.sql.ResultSet rs = ps.executeQuery();
            
            if (!rs.next()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Current password is incorrect", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            java.sql.PreparedStatement ps2 = conn.prepareStatement("UPDATE USERS SET PASSWORD=?");
            ps2.setString(1, newPass);
            ps2.executeUpdate();
            ps2.close();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Password changed successfully ✔", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            dispose();
            new Login().setVisible(true);
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

