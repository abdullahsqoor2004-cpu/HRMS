    private void addShowHideButton(javax.swing.JPasswordField field) {
        javax.swing.JButton btn = new javax.swing.JButton("👁");
        btn.setPreferredSize(new java.awt.Dimension(45, field.getPreferredSize().height));
        btn.setFocusable(false);
        btn.addActionListener(e -> {
            if (field.getEchoChar() == 0) {
                field.setEchoChar('•');
                btn.setText("👁");
            } else {
                field.setEchoChar((char) 0);
                btn.setText("🙈");
            }
        });
        field.setLayout(new java.awt.BorderLayout());
        field.add(btn, java.awt.BorderLayout.EAST);
    }
